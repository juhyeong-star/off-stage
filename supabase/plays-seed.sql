-- Off-Stage — 재생수 초기값 채우기 (1회성)
-- STO 전 데모 단계 — 곡이 너무 휑해 보이지 않게 기존 곡들에 1,000씩 깔아둠.
-- 안전장치: plays_count = 0 인 곡만 건드림 → 이미 실제로 쌓인 재생수(진짜 데이터)는
-- 절대 덮어쓰지 않음. 이 파일은 "한 번만" 실행할 것 (재실행해도 0인 곡만 다시 채워질 뿐,
-- 이미 1,000 이상인 곡은 안 건드리지만 습관적으로 여러 번 돌릴 필요는 없음).
-- =====================================================================

update public.tracks set plays_count = 1000 where plays_count = 0;
